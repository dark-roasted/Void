function t(lang, key, params = {}) {
    let text = translations[lang]?.[key] || translations['en'][key];
    
    if (!text) return key;

    for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, v);
    }
    return text;
}

